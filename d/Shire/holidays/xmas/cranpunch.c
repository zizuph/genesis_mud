inherit "/std/drink";

void
create_drink()
{
    set_name("punch");
    add_name("cranberry punch");
    add_name("glass of cranberry punch");
    set_pname("punches");
    add_pname("cranberry punches");
    add_pname("glass of cranberry punch");
    set_short("glass of cranberry punch");
    set_pshort("glasses of cranberry punch");
    set_long("This is a glass of cranberry punch. It is made of cranberry juice, mixed with orange and lemon juices. Sugar has been added to make it sweeter, and nutmeg, cloves, cinammon, and allspice have been mixed in to add flavor to the drink.\n");

    set_soft_amount(90);

}
