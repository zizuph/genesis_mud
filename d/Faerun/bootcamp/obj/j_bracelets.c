inherit "/std/armour";

#include <stdproperties.h>
#include <wa_types.h>

void
create_armour()
{ 

    set_name("bracelets");

    add_name("bracelets");

    set_short("wide silver bracelet on each wrist");

    set_adj("wide");

    add_adj("silver");

    set_long("These bracelets are wide and silver.\n");
    
    set_at(A_WRISTS);

    set_ac(20);

}