/* A piece of broccoli for the grocery shop in Gelan */

inherit "/d/Calia/std/fruit";

void
create_fruit()
{
   set_name("broccoli");
   set_short("piece of broccoli");
   set_pshort("pieces of broccoli");
   set_long("A large piece of green broccoli. Yum.\n");
   set_fruit_amount(5);
   set_mana_add(10);
   set_hp_add(20);
   set_fatigue_add(10);
}

