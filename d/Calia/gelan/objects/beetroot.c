/* A beetroot for the grocery shop in Gelan */

inherit "/d/Calia/std/fruit";

void
create_fruit()
{
   set_name("beetroot");
   set_short("beetroot");
   set_pshort("beetroots");
   set_long("A large red beetroot.\n");
   set_fruit_amount(3);
   set_mana_add(7);
   set_hp_add(0);
   set_fatigue_add(7);
}

