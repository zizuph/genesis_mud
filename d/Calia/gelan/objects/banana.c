/* A large banana for the grocery shop in Gelan */

inherit "/d/Calia/std/fruit";

void
create_fruit()
{
   set_name("banana");
   set_short("large banana");
   set_adj("large");
   set_long("A large banana.\n");
   set_fruit_amount(5);
   set_mana_add(0);
   set_hp_add(20);
   set_fatigue_add(0);
}

